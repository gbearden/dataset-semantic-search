# ── MockEmbedder ─────────────────────────────────────────────────────────────


class MockEmbedder:
    """
    Deterministic embedder for unit tests — no model download required.

    Vectors are generated by hashing the text, providing stable orderings
    so that structural assertions (rank, method, non-empty) hold without
    a real language model.
    """

    DIM = 64

    @property
    def dim(self) -> int:
        return self.DIM

    @property
    def model_name(self) -> str:
        return "mock-embedder"

    def encode(self, texts, batch_size=64, normalize=True, show_progress=False):
        single = isinstance(texts, str)
        if single:
            texts = [texts]
        vecs = []
        for t in texts:
            h = hashlib.sha256(t.encode()).digest()
            # repeat hash bytes to fill DIM floats
            raw = np.frombuffer(h * (self.DIM // len(h) + 1), dtype=np.uint8)[: self.DIM]
            v = raw.astype(np.float32) - 128.0
            if normalize and np.linalg.norm(v) > 0:
                v /= np.linalg.norm(v)
            vecs.append(v)
        arr = np.stack(vecs)
        return arr[0] if single else arr


